#!/usr/bin/env node

/**
 * Test script for BrandCraft deployment functionality
 * Run this script to test the deployment API endpoints
 */

const fetch = require('node-fetch');
require('dotenv').config();

const API_BASE = 'http://localhost:3001';

// Test HTML content
const testHTML = `<!DOCTYPE html>
<html>
<head>
    <title>Test Brand</title>
    <style>
        body { font-family: Arial, sans-serif; padding: 40px; text-align: center; }
        h1 { color: #7C4DFF; }
        .btn { background: #7C4DFF; color: white; padding: 15px 30px; border: none; border-radius: 50px; cursor: pointer; }
    </style>
</head>
<body>
    <h1>Test Brand Website</h1>
    <p>This is a test website generated by BrandCraft</p>
    <button class="btn">Get Started</button>
</body>
</html>`;

async function testDeployment() {
    console.log('🚀 Testing BrandCraft Deployment API...\n');

    // Check if required environment variables are set
    const required = ['VERCEL_TOKEN'];
    const missing = required.filter(key => !process.env[key]);

    if (missing.length > 0) {
        console.error('❌ Missing required environment variables:');
        missing.forEach(key => console.error(`   - ${key}`));
        console.log('\nPlease set these in your .env file');
        process.exit(1);
    }

    console.log('✅ Environment variables check passed');

    try {
        console.log('\n📡 Sending deployment request...');

        const response = await fetch(`${API_BASE}/api/deploy`, {
            method: 'POST',
            headers: {
                'Content-Type': 'application/json',
            },
            body: JSON.stringify({
                html: testHTML,
                brandName: 'TestBrand'
            })
        });

        console.log(`📡 Response status: ${response.status}`);

        if (!response.ok) {
            const error = await response.json();
            console.error('❌ Deployment failed:', error.error);
            return;
        }

        const result = await response.json();
        console.log('✅ Deployment successful!');
        console.log('\n📋 Deployment Details:');
        console.log(`   URL: ${result.url}`);
        console.log(`   Deployment ID: ${result.deploymentId}`);

        console.log('\n🌐 You can now visit your deployed website at:');
        console.log(`   ${result.url}`);

    } catch (error) {
        console.error('❌ Test failed:', error.message);
        console.log('\n💡 Troubleshooting tips:');
        console.log('   1. Make sure the BrandCraft server is running (npm start)');
        console.log('   2. Verify your Vercel token is valid');
        console.log('   3. Check your internet connection');
        console.log('   4. Ensure Vercel API is accessible');
    }
}

// Run the test
testDeployment();